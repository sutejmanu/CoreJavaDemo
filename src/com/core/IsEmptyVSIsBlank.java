package com.core;

public class IsEmptyVSIsBlank {
    public static void main(String[] args) {

        System.out.println("----IsEmpty---------");
        System.out.println("".isEmpty());
        System.out.println(" ".isEmpty());
        System.out.println("\n".isEmpty());


        System.out.println("----IsBlank---------");
        System.out.println("".isBlank());
        System.out.println(" ".isBlank());
        System.out.println("\n".isBlank());
    }
}
